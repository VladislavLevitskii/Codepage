const item1 = document.querySelector(".priceandarrow1");
const description1 = document.querySelector(".description1");
const arrow1 = document.querySelector(".arrow1");
function toggleMenu1() {
    if (description1.classList.contains("showMenu")) {
    description1.classList.remove("showMenu")
    arrow1.style.transform = "rotate(0deg)";
    }   else {
        description1.classList.add("showMenu")
        arrow1.style.transform = "rotate(180deg)";
        }
}
item1.addEventListener("click", toggleMenu1);

